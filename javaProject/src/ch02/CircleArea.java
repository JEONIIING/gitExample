package ch02;

public class CircleArea {
	//문제2]상수를 활용한 원의 면적을 구하는 프로그램 작성
	public static void main(String[] args) {

		final double PI = 3.14;
		double r = 10.0 ; 
		double area = r * r * PI;
		
		System.out.println("==================");
		System.out.println("원의 면적 = " + area);
		System.out.println("==================");
		
		
		
		
	}

}
